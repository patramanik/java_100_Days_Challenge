public class IntToFlot {
    public static void main(String[] args) {
        System.out.println("Int To Flote(automatically):");
        int myInt = 22;
        float myFlote = myInt;
        System.out.println(myInt);
        System.out.println(myFlote);

        System.out.println("Flote To Int(manually):");

        float myFlote2 = 45.52f;
        int myInt2 = (int)myFlote2;
        System.out.println(myFlote2);
        System.out.println(myInt2);
    }
}
